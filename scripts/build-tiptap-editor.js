const fs = require('fs');
const path = require('path');
const esbuild = require('esbuild');

const root = path.resolve(__dirname, '..');
const entryFile = path.join(root, 'src', 'editor', 'tiptap-editor-entry.ts');
const outDir = path.join(root, 'src', 'generated');
const outJs = path.join(outDir, 'tiptap-editor.bundle.js');
const outTs = path.join(outDir, 'tiptap-editor-bundle.ts');

fs.mkdirSync(outDir, {recursive: true});

esbuild.buildSync({
  bundle: true,
  entryPoints: [entryFile],
  format: 'iife',
  globalName: 'KolamTipTapBundle',
  legalComments: 'none',
  logLevel: 'info',
  minify: true,
  outfile: outJs,
  platform: 'browser',
  sourcemap: false,
  target: ['chrome109'],
});

const bundledSource = fs.readFileSync(outJs, 'utf8');
const generatedSource = [
  '// This file is generated by scripts/build-tiptap-editor.js.',
  '// Do not edit manually.',
  `export const tipTapEditorBundle = ${JSON.stringify(bundledSource)};`,
  '',
].join('\n');

fs.writeFileSync(outTs, generatedSource, 'utf8');
fs.rmSync(outJs, {force: true});

console.log(`Generated ${path.relative(root, outTs)}`);
